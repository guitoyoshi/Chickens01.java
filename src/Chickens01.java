public class Chickens01 {
    public static void main(String[] args) {
        int count=15; //contagem inicial de ovos.
        int a, b, c;  //segunda, terça e quarta.
        a = count;        //contagem de ovos de segunda com três galinhas.
        b = count + 5;    //contagem de ovos de terça com mais de uma galinha.
        c = count - 5;    //contagem de ovos de quarta com menos de uma galinha. 
        
        System.out.println("segunda feira = " + a + ", ");
        System.out.println("terça feira = " + b + ", ");
        System.out.println("querta feira = " + c + ", ");
    }   
}
